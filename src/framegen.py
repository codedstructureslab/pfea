			[0.0 ,0.5 ,0.75],
			[0.0 ,0.75,0.5 ],
			[0.25,0.0 ,0.5 ],
			[0.25,0.25,0.25],
			[0.25,0.25,0.75],
			[0.25,0.5 ,0.0 ],
			[0.25,0.5 ,1.0 ],
			[0.25,0.75,0.25],
			[0.25,0.75,0.75],
			[0.25,1.0 ,0.5 ],
			[0.5 ,0.0 ,0.25],
			[0.5 ,0.0 ,0.75],
			[0.5 ,0.25,0.0 ],
			[0.5 ,0.25,1.0 ],
			[0.5 ,0.75,0.0 ],
